// Get artist info with fuzzy matching
export function getArtistInfo(artistName) {
  if (!artistName) return null;
  
  const clean = artistName.replace(/ -$/, '').trim();
  
  // Direct match
  if (artistDatabase[clean]) return { ...artistDatabase[clean], name: clean };
  
  // Case-insensitive match
  const lowerClean = clean.toLowerCase();
  for (const [key, value] of Object.entries(artistDatabase)) {
    if (key.toLowerCase() === lowerClean) return { ...value, name: key };
  }
  
  // Partial match
  for (const [key, value] of Object.entries(artistDatabase)) {
    if (key.toLowerCase().includes(lowerClean) || lowerClean.includes(key.toLowerCase())) {
      return { ...value, name: key };
    }
  }
  
  return null;
}

// Generate SEO description paragraphs for a song page
export function generateSeoContent(song, artist) {
  const artistInfo = getArtistInfo(artist);
  const cleanArtist = artist?.replace(/ -$/, '').trim() || 'this artist';
  
  if (artistInfo) {
    const otherSongs = artistInfo.notableSongs
      .filter(s => s.toLowerCase() !== song?.toLowerCase())
      .slice(0, 3)
      .join(', ');
    
    return {
      paragraph1: `${artistInfo.name} is ${artistInfo.description}. Active from ${artistInfo.era}, they have influenced countless musicians and continue to be a cornerstone of ${artistInfo.genre} music.`,
      paragraph2: `DadRockTabs provides guitar and bass tab lessons for classic ${artistInfo.name} songs including ${song}${otherSongs ? `, ${otherSongs}` : ''}, and more. Learn to play their most iconic riffs and bass lines with free video tutorials that break down every note.`,
      paragraph3: `Each lesson features synchronized tablature with the original recording so you can follow along and learn the riffs exactly as they were played. Whether you're a beginner picking up your first power chord or an experienced player looking to nail every detail, our step-by-step approach makes it easy to master ${artistInfo.genre} guitar.`,
    };
  }
  
  // Generic fallback for unknown artists
  return {
    paragraph1: `${cleanArtist} is a talented artist in the world of rock and metal music, known for powerful guitar-driven songs that guitarists love to learn and play.`,
    paragraph2: `DadRockTabs provides guitar and bass tab lessons for ${cleanArtist} songs including ${song}. Learn to play their riffs and bass lines with free video tutorials that break down every note.`,
    paragraph3: `Each lesson features synchronized tablature with the original recording so you can follow along and learn the riffs exactly as they were played. Whether you're a beginner or an experienced player, our step-by-step approach makes it easy to master these classic songs.`,
  };
}
